var hovercard = document.getElementById("hovercard");
var gola = document.getElementById("gola");

hovercard.addEventListener(
  "mouseover",
  () => (
    (hovercard.style.backgroundColor = "#B1B493"),
    (hovercard.style.color = "white")
  )
);
hovercard.addEventListener(
  "mouseleave",
  () => (
    (hovercard.style.backgroundColor = "white"),
    (hovercard.style.color = "black")
  )
);
hovercard.addEventListener(
  "mouseover",
  () => (gola.style.backgroundColor = "white")
);
hovercard.addEventListener("mouseover", () =>
  setTimeout(() => {
    gola.style.marginLeft = "5rem";
  }, 50)
);
hovercard.addEventListener(
  "mouseleave",
  () => (gola.style.backgroundColor = "#B1B493")
);
hovercard.addEventListener("mouseleave", () =>
  setTimeout(() => {
    gola.style.marginLeft = "2rem";
  }, 50)
);


